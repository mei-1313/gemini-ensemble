# --- Compatibility Wrappers ---

class BaseReducer(ABC):
    """
    Abstract base class compatibility wrapper.
    """
    def __init__(self, reducer_model: Optional[str] = None):
        self.reducer_model = reducer_model

    @abstractmethod
    async def reduce(
        self,
        client: genai.Client,
        fallback_model: str,
        prompt: str,
        outputs: List[str],
        response_schema: Optional[Any] = None,
        output_language: Optional[str] = None,
        **kwargs
    ) -> Any:
        pass


class VotingReducer(BaseReducer):
    """
    Voting Reducer compatibility wrapper class.
    """
    async def reduce(
        self,
        client: genai.Client,
        fallback_model: str,
        prompt: str,
        outputs: List[str],
        response_schema: Optional[Any] = None,
        output_language: Optional[str] = None,
        **kwargs
    ) -> Any:
        return await reduce_voting(
            client=client,
            fallback_model=fallback_model,
            prompt=prompt,
            outputs=outputs,
            response_schema=response_schema,
            reducer_model=self.reducer_model,
            output_language=output_language,
            **kwargs
        )


class CriticReducer(BaseReducer):
    """
    Critic Reducer compatibility wrapper class.
    """
    async def reduce(
        self,
        client: genai.Client,
        fallback_model: str,
        prompt: str,
        outputs: List[str],
        response_schema: Optional[Any] = None,
        output_language: Optional[str] = None,
        **kwargs
    ) -> Any:
        return await reduce_critic(
            client=client,
            fallback_model=fallback_model,
            prompt=prompt,
            outputs=outputs,
            response_schema=response_schema,
            reducer_model=self.reducer_model,
            output_language=output_language,
            **kwargs
        )
